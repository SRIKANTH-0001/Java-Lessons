package Main;
public class Main{
	public static void main(String[] args) {
        //Creation of Variables With Different Datatypes

        int a=15;
        System.out.println(a);

        float a1=15.23f;
        System.out.println(a1);
        
        double a2=37.371636164236;
        System.out.println(a2);
        
        long a3=1364714613746263L;
        System.out.println(a3);
        
        char c='#';
        System.out.println(c);
        
        String s="I'm Srikanth";
        System.out.println(s);
        
        boolean b=true;
        System.out.println(b);
    }
}